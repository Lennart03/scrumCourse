package com.realdolmen.domain;

public class Candidate {
	
	private String name;
	
	

	public Candidate() {
		super();
	}



	public Candidate(String name) {
		super();
		this.name = name;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}
	
	

}
